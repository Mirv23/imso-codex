# ── Blog ─────────────────────────────────────────────────────────

@pytest.mark.django_db
class TestBlog:
    def test_dashboard_create_and_public_visibility(self):
        client = Client()
        _staff(client)
        r = client.post(reverse("adminpanel:blog-create"),
                        data=json.dumps({"title": "Bien épargner", "body": "Corps de l'article.", "status": "published"}),
                        content_type="application/json")
        assert r.status_code == 201
        post = BlogPost.objects.get(id=r.json()["id"])
        assert post.published_at is not None  # auto-renseigné à la publication

        # Public : liste + détail visibles
        public = Client()
        resp = public.get(reverse("core:blog_list"))
        assert resp.status_code == 200
        assert b"Bien" in resp.content
        detail = public.get(reverse("core:blog_detail", args=[post.slug]))
        assert detail.status_code == 200

    def test_draft_not_public(self):
        draft = BlogPost.objects.create(title="Brouillon secret", body="…", status=BlogPost.Status.DRAFT)
        public = Client()
        # Absent de la liste
        resp = public.get(reverse("core:blog_list"))
        assert b"Brouillon secret" not in resp.content
        # Détail introuvable (404)
        assert public.get(reverse("core:blog_detail", args=[draft.slug])).status_code == 404

    def test_slug_autogenerated(self):
        post = BlogPost.objects.create(title="Mon Premier Article", body="x")
        assert post.slug == "mon-premier-article"
